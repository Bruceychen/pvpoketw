  <div class="article-section">
		<p>A huge addition to the game is a third Status Move for certain Pokemon. These moves cost energy like other Charged Moves, but have special effects instead of dealing damage and can’t be used to bait. These moves add a whole new dynamic to the game. Here are a few examples of the new Status Moves and their effects and potential strategies!</p>
