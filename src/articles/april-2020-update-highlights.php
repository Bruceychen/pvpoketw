			<li><b>Bellossom: Sweet Scent - </b>50 energy, lowers your opponent’s Defense by 1 stage. In addition, a random Pokemon appears that both players must catch to return to battle. This move is excellent as part of timeout strategies, especially if you spawn the current legendary Pokemon. In the upper ranks, it’s recommended you toss all the balls in your inventory to escape this move’s effect.</li>
			<li><b>Ledian: Baton Pass - </b>60 energy, switches and transfers stat changes to the new Pokemon. This move works even when your switch isn’t available but restarts your switch clock. Combined with Silver Wind, you can now get that +2 Atk/+2 Def Registeel you’ve always wanted with a 90% chance to make your opponent rage quit. Note that Baton Pass stacks with the hooking changes described above, so expect double the number of fishing poles than normal.</li>
			<li><b>Hypno: Hypnosis - </b>75 energy, puts opponent to Sleep. While asleep, your opponent can’t attack and must complete a swiping Mareep minigame to wake up. Experienced players will be able to minimize their downtime. After Hypno lost the Community Day vote to Stone Edge Shuckle (a move Shuckle already had), Niantic decided to swing the pendulum in Hypno’s direction this update. If you’re one of those crazy people who plays with the sound on, Hypnosis emits high-pitched tones that have a chance to put you to sleep as well. This is particularly dangerous against players who are in bed rushing to complete their GO Battle League sets before midnight.</li>
			<li><b>Snorlax: Belly Drum - </b>100 energy, increases Attack by 4 stages. This is a go big or go home move that reaches Lick breakpoints in the stratosphere and is banned on Snorlax Libre in tournament play.</li>
