  <a name="dewgong"></a>
  <h3 class="article-header">Dewgong</h3>
  <div class="article-section">
  <p>
  <strong>Moveset: </strong>
  <em>Ice Shard</em>
  + <em>Icy Wind</em> &amp; Water Pulse
  <em>
  	<br/>
  </em>
  <strong>Formats: </strong>
  Great League
  </p>
  <p>
  If you’ve been playing Great League or in The Silph Arena for a while,
  you’ve probably heard myths about this rare creature. You’ve maybe even
  glimpsed one for 4 whole minutes before the game timed out. The fabled
  double legacy Dewgong is available via Elite TM, but comes at the steep
  cost of two TM's if you don’t have one of the legacy moves already.
  </p>
  <p>
  Make no mistake,    <strong>double legacy Dewgong is a top tier Great League Pokemon </strong>
  that combines great bulk with a stat-lowering move that is both spammable
  and powerful. Unlike comparable moves like Bubble Beam and Sand Tomb, Icy
  Wind boasts decent damage. Even in losing matchups, Icy Wind draws
  opponents into extended fights where they may lose the battle as well as
  their cool.
  </p>
  <p>
  One challenge is <strong>Dewgong can be difficult to build a team around</strong>, especially in GO Battle League. Dewgong is vulnerable to Counter users, Rock Slide from Galarian Stunfisk, Bastiodon, Electric types like Galvantula, and bulky Grass-types like Ferrothorn. One successful example of a Dewgong team is Dewgong/Medicham/Munchlax run by <a href="https://twitter.com/TheAsianMilkMan" target="_blank">TheAsianMilkMan</a>.
  </p>
  <p>
  Yes, Dewgong does need both Ice Shard and Icy Wind to reach its full
  potential. Frost Breath + Icy Wind is surprisingly playable but faster Icy
  Winds are essential for Dewgong’s effectiveness.
  </p>
  <p>
  A word of caution: Dewgong could potentially have its removed moves
  returned in a future update, or Icy Wind could be nerfed following the fate
  of similar moves like Power-Up Punch and Ancient Power. This goes for the other Pokemon in this article, but only use Elite TM’s
  on Dewgong now if you want to win with it now.
  </p>
  </div>

  <a name="lapras"></a>
  <h3 class="article-header">Lapras</h3>
  <div class="article-section">
  <p>
  <strong>Moveset: </strong>
  <em>Ice Shard</em>
  + Surf & Skull Bash or <em>Ice Beam</em>
  <em>
	  <br/>
  </em>
  <strong>Formats: </strong>
  Great League, Ultra League, Premier Cup, Silph Arena
  </p>
  <p>
	  The second Water/Ice type in this article, this majestic Pokemon is a powerful contender in most formats where it's allowed thanks to its bulk and powerful moveset. <b>Lapras is a top pick in the Ultra League Premier Cup</b> in particular. You'll need the legacy move Ice Shard. After that, Surf is always one of its preferred Charged Moves, followed by Skull Bash in Great League and Premier Cup, which hits hard and raises Lapras's defenses even further. If you're playing in regular Ultra League, you may want to consider another Elite TM on Ice Beam for super effective hits against Giratina.
  </p>
  <p>In the Premier Cup, Lapras is one of the bulkiest Pokemon and synergizes well with starters like Charizard and Venusaur. It's forgiving to use and can dish out damage even in its bad matchups. If you don't have an Ice Shard Lapras in your collection, it's one of the best Elite TM candidates to date.</p>
  <p><b>Shadow Lapras has tradeoffs with regular Lapras</b>, but offers a harder hitting variant. In Great League, Shadow Lapras's Surf attacks are strong enough to take on Galarian Stunfisk, which regular Lapras can struggle with. It's a steep cost for Ultra League and doesn't provide many advantages over regular Lapras, but if you have the resources, more power to you!</p>
  </div>

	<a name="zapdos"></a>
	<h3 class="article-header">Zapdos</h3>
  <div class="article-section">
<p>
    <strong>Moveset: </strong>
    <em>Thunder Shock</em>
    + Drill Peck &amp; Thunderbolt or Thunder
    <em>
        <br/>
    </em>
    <strong>Formats: </strong>
    Great League, Ultra League
</p>
<p>
    Thunder Shock Zapdos flew into raids a long while back, and although Zapdos
    became Great League eligible as a research breakthrough encounter, Great League Zapdos doesn't have access to Thunder Shock unless you use an Elite TM.
</p>
<p>
    Zapdos combines Charizard-like stats with one of the best defensive typings
    in the game—Electric and Flying—which is only weak to Ice and Rock. Thunder
    Shock and similar moves like Mud Shot and Psycho Cut are second only to
    Lock On in energy generation, giving Zapdos rapid access to Drill Peck and Thunderbolt.
</p>
<p>
    While it's strong and fast, <strong>Galarian Stunfisk poses a significant challenge to Zapdos's viability.</strong> If you can stomach that weakness or find a meta without Galarian Stunfisk, it has positive matchups against other meta players like Swampert, Skarmory, Tropius, and
    Toxicroak. It also stands up to future Great League giants like Jellicent
    and Mandibuzz.
</p>
<p>
    One of the most important matchups in the Great League is Azumarill, and here's where things get tricky for Zapdos. Below is a list of conditions for Zapdos to beat high-rank Azumarill in a direct 1-shield matchup:
	<ul>
		<li><a href="https://pvpoke.com/battle/1500/zapdos-16-11-2-5-4-4-0/azumarill-40-8-15-15-4-4-1/11/1-3-5/0-2-1/">Thunderbolt Zapdos</a> needs to hit a Thunder Shock breakpoint against Azumarill for its Thunderbolt to KO. This requires steep Attack IV's against high-rank Azumarill (11/2/5 guarantees the breakpoint against rank 1 Azumarill).</li>
		<li><a href="https://pvpoke.com/battle/1500/zapdos-15-15-14-12-4-4-0/azumarill-40-8-15-15-4-4-1/11/1-3-4/0-2-1/">Thunder Zapdos</a> needs 138.6 Attack (15 Attack IV for a research breakthrough Zapdos) for Thunder to KO a rank 1 Azumarill.</li>
		<li>Barring this, Thunder Zapdos should allow Azumarill to use its Charged Move first and attempt to <a href="https://pvpoke.com/battle/sandbox/1500/zapdos-15-10-14-12-4-4-0/azumarill-40-8-15-15-4-4-1/22/1-3-4/0-2-1/16.110100-19.101100-32.101000/">sneak in extra Thunder Shocks</a>. This extra difference is enough for Thunder to KO.</li>
		<li>All Zapdos lose head-on shields down and all Zapdos win investing two shields.</li>
	</ul>
</p>
<p>Should you run Thunderbolt or Thunder? For regular Zapdos, your decision could depend on the scenarios outlined above. Thunderbolt is 90 power/55 energy while Thunder is 100 power/60 energy. The first Charged Move takes 7 Thunder Shocks to reach but the second Thunderbolt only takes 6. This one may come down to personal preference but either work.</p>
<p>
    So you could get some Attack, but what if you wanted <em>more </em>Attack?
Enter Shadow Zapdos.    <strong>Shadow Zapdos locks down the Azumarill matchup </strong>and also
    picks up Sunny Cherrim, Jirachi, Scrafty, Umbreon, and Venusaur—and
    potentially Dewgong, Froslass, and Sableye with bait. This comes at the
    cost of Meganium and Swampert so there are tradeoffs. In general Shadow
    Zapdos is highly dangerous especially with energy; it has comparable Attack
    to Haunter with 20% more bulk. Thunderbolt is recommended for Shadow Zapdos so you can reach it slightly faster.
</p>
<p>
    In the Ultra League, Shadow Zapdos takes on meta staples such as Articuno,
    Poliwrath, Venusaur, Swampert, and Registeel. It’s rated as one of the
    safest switches that can force shields and take all but Giratina and
    Melmetal into the red.
</p>
	</div>

