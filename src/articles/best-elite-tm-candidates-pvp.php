  <a name="cresselia"></a>
  <h3 class="article-header">克雷色利亞 (Cresselia)</h3>
<div class="article-section">
<p>
  <strong>推薦招式：</strong>
    精神利刃 或 念力
  + <em>打草結</em> &amp; 月亮之力
  <em>
	  <br/>
  </em>
  <strong>適用於：</strong>
    超級聯盟(CP1500)
</p>
<p>
    超級聯盟充斥者許多水屬性寶可夢，常見如瑪力露麗和巨沼怪，它們是隊伍中的中堅力量。學習到打草結的克雷色利亞能有效在戰鬥中，<b>壓制並擊敗瑪力露麗、鯰魚王、巨沼怪，和白海獅。</b>他還可以擊敗許多草系寶可夢，例如妙蛙花和熱帶龍等，以及格鬥系的寶可夢，如毒骷蛙、恰雷姆等和偽格鬥寶可夢，如防禦型代歐奇希斯和過動猿等。
</p>
<p>
    打草結通常與月亮之力搭配使用，他使克雷色利亞可以對抗惡屬性寶可夢例如月亮伊布、頭巾混混。對於快速招式精神利刃和念力的選擇，取決於你想如何的使用克雷色利亞。念力傷害量高(尤其對付帶有毒屬性及格鬥屬性的寶可夢更是如此)，而精神利刃給予了克雷色利亞在擋切及蓄能上更大的靈活度。對於精神利刃和念力選擇可能取決於你的團隊組成，比如你需要使用克雷色利亞對抗惡屬性寶可夢。
</p>
<p>
    超級聯盟(CP1500)中推薦用打草結，但在一般情況下月亮之力和預知未來通常是標配。
</p>
  </div>

  <a name="dewgong"></a>
  <h3 class="article-header">白海獅 (Dewgong)</h3>
  <div class="article-section">
  <p>
  <strong>推薦招式：</strong>
  <em>冰礫</em>
  + <em>冰凍之風</em> &amp; 水之波動
  <em>
  	<br/>
  </em>
  <strong>適用於：</strong>
    超級聯盟(CP1500)
  </p>
  <p>
      如果你在超級聯盟、西爾佛(Silph)聯盟或天梯(Go Battle League)投注了不少時間，你可能聽過他的神話。他可以單槍匹馬就耗了你整整近四分鐘的遊戲時間直到對戰時間結束。白海獅的絕版招皆可使用厲害招式學習器獲得，但如果你兩個絕版招都沒有，你可能就需要耗費可觀的資源-兩個厲害招式學習器來獲得這隻白海獅。
  </p>
  <p>
      毫無疑問的，<strong>雙絕版招的白海獅在超級聯盟中屬於頂級的寶可夢。</strong>他可以持續降低對手攻擊力，但又與泡沫光線和流沙地獄不同，冰凍之風同時具備不錯的傷害能力。即使白海獅遇上逆風/屬性不利的對戰，對手也有可能因為冰凍之風導致戰鬥時間被延長，進而被白海獅逆轉/打敗。
  </p>
  <p>
      在超級聯盟中，<strong>編組包含白海獅的隊伍是非常具有挑戰性的。</strong>因為岩石系(伽勒爾泥巴魚的岩崩、護城龍)電系(電蜘蛛)和草系(堅果啞鈴)都可以剋制白海獅。國外訓練家<a href="https://twitter.com/TheAsianMilkMan" target="_blank">TheAsianMilkMan</a>的白海獅+恰雷姆+小卡比獸 就是白海獅隊伍組成的一個成功例子。
  </p>
  <p>
      確實，白海獅需要冰礫 + 冰凍之風來充分發揮他的潛力。但冰息+ 冰凍之風的組合卻也可以大幅增加白海獅對戰中的靈活性。
  </p>
  <p>
      但這邊也提醒各位訓練家：白海獅冰凍之風的附加效果或攻擊力，很可能會隨著遊戲更動而像增強拳和原始之力一樣被削弱，也可能營運方突然又解除絕版，使用普通的特殊招式學習器就能學會...而這些潛在風險也同樣適用於本篇文章中其他寶可夢的絕版招式。但如果你現在就想贏，現在就想和絕版招白海獅共同戰鬥，那就使用厲害招式學習器吧！
  </p>
  </div>

  <a name="lapras"></a>
  <h3 class="article-header">拉普拉斯 (Lapras)</h3>
  <div class="article-section">
  <p>
  <strong>推薦招式：</strong>
  <em>冰礫</em>
  + 衝浪 & 火箭頭錘 or <em>冰凍光束</em>
  <em>
	  <br/>
  </em>
  <strong>適用於：</strong>
  超級、高級聯盟，紀念盃、Silph聯盟
  </p>
  <p>此篇推薦文的第二隻 水/冰 屬性寶可夢。憑藉其優秀的坦度及高傷害輸出的招式組，拉普拉斯得以和其他meta寶可夢列入隊伍組合的優先選擇，<b>尤其在高級紀念盃中更是常見的一員。</b>絕版一般招式：冰礫是不二選擇。特殊招式部分，衝浪永遠都是此寶可夢的標配，搭配上高傷害以及能強化自身防禦的火箭頭槌，拉普拉斯將能在超級聯盟及紀念盃中有更佳的表現。如果你專攻於"非"紀念盃(包含傳說寶可夢)高級聯盟，你也可以考慮多使用一個厲害招式機以習得冰凍光束。此技能在對上騎拉帝納等傳說寶可夢時能有較佳的傷害輸出。</p>
  <p>在紀念盃環境下，拉普拉斯的坦度(傷害耐受能力)可以稱得上是排名前幾，同時也具有較佳的先發隊員協同配合能力，比如搭配先發噴火龍或先發妙蛙花，拉普拉斯便能擔當一個優秀的補助與安全替換手。即便是在劣勢的對戰狀況，拉普拉斯的操作容錯度以及傷害輸出依然可以給對手不少的壓力甚至扳回一城。如果你還沒擁有冰礫拉普拉斯，那他絕對可以作為你使用厲害招式學習器的首選！</p>
  <p><b>暗影化拉普拉斯和一般拉普拉斯則是各有好壞</b>，但暗影化則是有更好的傷害輸出。比如在超級聯盟環境下，暗影拉普拉斯的衝浪對伽勒爾型態泥巴魚更有威脅，而一般的拉普拉斯卻容易陷入苦戰。而在高級聯盟狀況下，暗影拉普拉斯需要耗費大量的資源，且跟一般拉普拉斯相比也沒有較多的優勢，但如果你願意投入，你還是能獲得可觀的傷害回報。</p>
  </div>

  <a name="ho-oh"></a>
  <h3 class="article-header">鳳王</h3>
  <div class="article-section">
  <p>
  <strong>推薦招式：</strong>
  燒盡 + 勇鳥猛攻 & <em>地震</em>
  <em>
	  <br/>
  </em>
  <strong>適用於： </strong>
  大師聯盟(CP無限制)
  </p>
  <p>Ho-Oh received Earthquake as an exclusive move alongside Fire Punch Groudon and Grass Knot Cresselia. At the time it didn't spark much interest - not until the other shoe dropped with Incinerate. Now able to put its best foot forward, Ho-Oh with Incinerate and Earthquake is capable of contending with the best of Master League.</p>
  <p>Ho-Oh can challenge the reigning master, Dialga, as well as other key targets like Groudon, Togekiss, and Mewtwo. It can even survive a 2x super effective Rock Slide from Melmetal, and Brave Bird puts a decent dent into threats like Kyogre and Giratina. All in all, Ho-Oh has the moves and stats to be competitive.</p>
  <p>It's not all sunshine for the Rainbow Pokemon, however. Incinerate is the longest Fast Move in the game. Clocking in at 5 turns (1 turn longer than Confusion), Incinerate can be extremely cumbersome to use. If a Fast Move doesn't register properly or if you "overtap" to fire Incinerate instead of a Charged Move, matchups can quickly go south. Ho-Oh's fastest move (Brave Bird) also drops its Defense by 3 stages. Knowing when to Brave Bird and when to Earthquake will take a lot of experience to master.</p>
  <p>Overall, <b>Ho-Oh has a steep learning curve but is a worthy consideration</b> for an Elite TM.</p>
  </div>

	<a name="zapdos"></a>
	<h3 class="article-header">閃電鳥 (Zapdos)</h3>
  <div class="article-section">
<p>
    <strong>推薦招式：</strong>
    <em>電擊</em>
    + 啄鑽 &amp; 十萬伏特 或 打雷
    <em>
        <br/>
    </em>
    <strong>適用於：</strong>
    超級、高級聯盟
</p>
<p>
    Thunder Shock Zapdos flew into raids a long while back, and although Zapdos
    became Great League eligible as a research breakthrough encounter, Great League Zapdos doesn't have access to Thunder Shock unless you use an Elite TM.
</p>
<p>
    Zapdos combines Charizard-like stats with one of the best defensive typings
    in the game—Electric and Flying—which is only weak to Ice and Rock. Thunder
    Shock and similar moves like Mud Shot and Psycho Cut are second only to
    Lock On in energy generation, giving Zapdos rapid access to Drill Peck and Thunderbolt.
</p>
<p>
    While it's strong and fast, <strong>Galarian Stunfisk poses a significant challenge to Zapdos's viability.</strong> If you can stomach that weakness or find a meta without Galarian Stunfisk, it has positive matchups against other meta players like Swampert, Skarmory, Tropius, and
    Toxicroak. It also stands up to future Great League giants like Jellicent
    and Mandibuzz.
</p>
<p>
    One of the most important matchups in the Great League is Azumarill, and here's where things get tricky for Zapdos. Below is a list of conditions for Zapdos to beat high-rank Azumarill in a direct 1-shield matchup:
	<ul>
		<li><a href="https://pvpoke.com/battle/1500/zapdos-16-11-2-5-4-4-0/azumarill-40-8-15-15-4-4-1/11/1-3-5/0-2-1/">Thunderbolt Zapdos</a> needs to hit a Thunder Shock breakpoint against Azumarill for its Thunderbolt to KO. This requires steep Attack IV's against high-rank Azumarill (11/2/5 guarantees the breakpoint against rank 1 Azumarill).</li>
		<li><a href="https://pvpoke.com/battle/1500/zapdos-15-15-14-12-4-4-0/azumarill-40-8-15-15-4-4-1/11/1-3-4/0-2-1/">Thunder Zapdos</a> needs 138.6 Attack (15 Attack IV for a research breakthrough Zapdos) for Thunder to KO a rank 1 Azumarill.</li>
		<li>Barring this, Thunder Zapdos should allow Azumarill to use its Charged Move first and attempt to <a href="https://pvpoke.com/battle/sandbox/1500/zapdos-15-10-14-12-4-4-0/azumarill-40-8-15-15-4-4-1/22/1-3-4/0-2-1/16.110100-19.101100-32.101000/">sneak in extra Thunder Shocks</a>. This extra difference is enough for Thunder to KO.</li>
		<li>All Zapdos lose head-on shields down and all Zapdos win investing two shields.</li>
	</ul>
</p>
<p>Should you run Thunderbolt or Thunder? For regular Zapdos, your decision could depend on the scenarios outlined above. Thunderbolt is 90 power/55 energy while Thunder is 100 power/60 energy. The first Charged Move takes 7 Thunder Shocks to reach but the second Thunderbolt only takes 6. This one may come down to personal preference but either work.</p>
<p>
    So you could get some Attack, but what if you wanted <em>more </em>Attack?
Enter Shadow Zapdos.    <strong>Shadow Zapdos locks down the Azumarill matchup </strong>and also
    picks up Sunny Cherrim, Jirachi, Scrafty, Umbreon, and Venusaur—and
    potentially Dewgong, Froslass, and Sableye with bait. This comes at the
    cost of Meganium and Swampert so there are tradeoffs. In general Shadow
    Zapdos is highly dangerous especially with energy; it has comparable Attack
    to Haunter with 20% more bulk. Thunderbolt is recommended for Shadow Zapdos so you can reach it slightly faster.
</p>
<p>
    In the Ultra League, Shadow Zapdos takes on meta staples such as Articuno,
    Poliwrath, Venusaur, Swampert, and Registeel. It’s rated as one of the
    safest switches that can force shields and take all but Giratina and
    Melmetal into the red.
</p>
