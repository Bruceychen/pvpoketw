	<div class="faq-item">
		<h3 class="article-header"><strong>How good is Ursaluna with High Horsepower?</strong></h3>
		<div class="faq-answer">
			<p><b>Ursaluna has decent play in Master League formats as a non-Legendary Ground option.</b> High Horsepower is a high energy Charged Move comparable to Earth Power, and both Thunder Punch and Fire Punch provide cheap coverage options. Ursaluna's best Fast Move is Tackle, which may generate energy too slowly to be truly effective. All together, Ursaluna is viable in Master League and Master Premier Classic, but falls short of stronger Ground options like Excadrill, Garchomp, Groudon, and Landorus.</p>
			<p>Ursaluna currently doesn't have the stats or moves to be viable in Great League or Ultra League.</p>
		</div>
	</div>

	<div class="faq-item">
		<h3 class="article-header"><strong>Can my Shadow Teddiursa learn High Horsepower if I evolve it during Community Day?</strong></h3>
		<div class="faq-answer">
			<p><b>Shadow Pokemon must have Frustration removed in order to learn exclusive moves during Community Day.</b> Even if you unlock a second Charged Move, a Shadow Ursaluna that still has Frustration will not learn High Horsepower when it is evolved.</p>
			<p>If you have a Shadow Teddiursa with Frustration, do not evolve it during this Community Day and consider waiting to remove Frustration during the <a href="https://pokemongolive.com/en/post/greedy-gluttons/" target="_blank">Team Rocket takeover</a> beginning November 14th. You can then evolve your Shadow Teddiursa later during the December Community Day event to obtain High Horsepower on your Shadow Ursaluna.</p>
		</div>
	</div>

	<div class="faq-item">
		<h3 class="article-header"><strong>I have a Purified Teddiursa. Is Return any good and how can I obtain both Return and High Horsepower for Ursaluna?</strong></h3>
		<div class="faq-answer">
			<p>Return has some play for Ursaluna in Master League, hitting a variety of Pokemon for strong neutral damage. As a trade off, Return makes Ursaluna considerably slower and reduces its effectiveness while shields are in play.</p>
			<p><b>To keep Return on a Purified Ursaring, you must evolve it into Purified Ursaluna AFTER the Community Day evolution window. (WARNING: At the time of writing, High Horsepower is still learnable after the announced evolution window of 10pm local time. Consider waiting to evolve your Purified Ursaring.)</b> If you evolve a Purified Ursaring during the Community Day evolution window, the exclusive move High Horsepower will always overwrite Return, even if you have unlocked a second Charged Move. If this happens, you will be unable to restore Return, even via Elite TM.</p>
			<p>To obtain High Horsepower on a Purified Ursaluna that knows Return, you must unlock a second Charged Move and use an Elite TM. However, if Return becomes a preferred move on Ursaluna, it's unlikely that High Horsepower will be the move of choice alongside Return since both are high energy moves.</p>
