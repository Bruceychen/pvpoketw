		<h3 class="article-header"><strong>How good is Ursaluna with High Horsepower?</strong></h3>
		<div class="faq-answer">
			<p><b>Ursaluna has niche play in Master League formats but its underwhelming movepool holds it in back in general PvP.</b> High Horsepower is a high energy Charged Move comparable to Earth Power. However, Ursaluna's best Fast Move is Tackle, which is too slow to be effective with High Horsepower's energy cost. All together, Ursulana has some play in Master League and Master Premier Classic, but pales in comparison to stronger Ground types like Excadrill, Garchomp, Groudon, and Landorus. Ursaluna currently has no play in Great League or Ultra League.</p>
			<p>If Ursalana obtains the ability to learn Shadow Claw in the future, it would become much more viable.</p>
