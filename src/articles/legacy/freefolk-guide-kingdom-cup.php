		<p>Steelix is much more effective than its scorecard might indicate. Not pictured are its great matchups against flex picks like Alolan Marowak, Bronzong, and Dragonair. It also has great matchup synergy with multiple Pokemon, making it a great fit for any roster. The things that beat it are things your opponent won't want locked into a poor matchup, so Steelix can be a dangerous lead to draw out those targets.</p>
	  <p dir="ltr">A key component of playing Steelix is playing mind games with Crunch and Earthquake. Build up enough energy for Earthquake, and then make your opponent guess whether you&rsquo;re actually using Earthquake or baiting their shields with a low-energy Crunch. Mix this up as your opponent gains experience against you; if you bait shields with Crunch in the first round, try hitting them with Earthquake in the second and see if they&rsquo;ll let it through unblocked.	</p>
		<p dir="ltr">Steelix has previously enjoyed being a relatively safe choice and switch given its huge number of resistances, solid defenses, and few counters. It has a lot more to be wary of in the Kingdom Cup, though. Where Lapras preferred Ice-type attacks in Tempest, it&rsquo;ll likely be packing Water Gun here. And that&rsquo;s to say nothing of the Fighting-type Pokemon that Steelix has to contend with! If Steelix is your only answer to the Dragons, it may be better kept in reserve.</p>
		
