		<p>Hydro Pump may seem redundant with Waterfall, but if Empoleon finds itself closing with an energy lead (either from the previous fight or switching in with energy built), Hydro Pump is essential against targets like Steelix, Bastiodon, Lucario, Bronzong, and Alolan Marowak. However, a case can be made for Blizzard to specifically hit Altaria. The problem with Flash Cannon and Blizzard as a pairing is there many targets who resist both. Hydro Pump plus Blizzard is another possibility at the cost of the tiny bit of speed our emperor penguin has.</p>
